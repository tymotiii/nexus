static uint32_t *fb_address;
static uint64_t pitch_in_bytes;
static uint64_t fb_width;
static uint64_t fb_height;

static int start_x = 0;
static int start_y = 0;
static volatile int cursor_x;
static volatile int cursor_y;

static void halt(void) {
    while (1) { __asm__("hlt"); }
}

static inline void outb(uint16_t port, uint8_t val) {
    __asm__ volatile ("outb %0, %1" : : "a"(val), "Nd"(port));
}

static inline uint8_t inb(uint16_t port) {
    uint8_t ret;
    __asm__ volatile ("inb %1, %0" : "=a"(ret) : "Nd"(port));
    return ret;
}

void clear_screen(uint32_t color) {
    cursor_x = start_x;
    cursor_y = start_y;
    for (uint64_t y = 0; y < fb_height; y++) {
        for (uint64_t x = 0; x < fb_width; x++) {
            uint32_t *pixel = (uint32_t*)((uint8_t*)fb_address + (y * pitch_in_bytes) + (x * 4));
            *pixel = color;
        }
    }
}

void scroll_screen(void) {
    uint64_t line_bytes = fb_width * 4;
    uint8_t *src = (uint8_t*)fb_address + (16 * pitch_in_bytes);
    uint8_t *dst = (uint8_t*)fb_address;
    uint64_t copy_height = fb_height - 16;

    for (uint64_t y = 0; y < copy_height; y++) {
        uint64_t *d64 = (uint64_t*)(dst + (y * pitch_in_bytes));
        uint64_t *s64 = (uint64_t*)(src + (y * pitch_in_bytes));
        for (uint64_t i = 0; i < line_bytes / 8; i++) {
            d64[i] = s64[i];
        }
    }

    for (uint64_t y = copy_height; y < fb_height; y++) {
        uint32_t *pixel_line = (uint32_t*)((uint8_t*)fb_address + (y * pitch_in_bytes));
        for (uint64_t x = 0; x < fb_width; x++) {
            pixel_line[x] = 0x00000000;
        }
    }

    cursor_y = fb_height - 16;
    cursor_x = start_x;
}

__attribute__((sysv_abi)) void draw_char(uint64_t x, uint64_t y, char c, uint32_t color) {
    unsigned char idx = (unsigned char)c;
    if (idx > 127) idx = '?';
    const uint8_t *glyph = ibm_vga_font[idx];
    for (uint64_t cy = 0; cy < 16; cy++) {
        for (uint64_t cx = 0; cx < 8; cx++) {
            uint64_t screen_x = x + cx;
            uint64_t screen_y = y + cy;
            if (screen_x < fb_width && screen_y < fb_height) {
                volatile uint32_t *pixel = (volatile uint32_t*)((uint8_t*)fb_address + (screen_y * pitch_in_bytes) + (screen_x * 4));
                if (glyph[cy] & (1 << (7 - cx))) {
                    *pixel = color;
                } else if (c == ' ') {
                    *pixel = 0x00000000;
                }
            }
        }
    }
}

__attribute__((sysv_abi)) void terminal_putc(char c, uint32_t color) {
    if (c == '\n') {
        cursor_x = start_x;
        cursor_y += 16;
        if (cursor_y + 16 > (int)fb_height) scroll_screen();
        return;
    }
    if (cursor_x + 8 > (int)fb_width) {
        cursor_x = start_x;
        cursor_y += 16;
        if (cursor_y + 16 > (int)fb_height) scroll_screen();
    }
    draw_char((uint64_t)cursor_x, (uint64_t)cursor_y, c, color);
    cursor_x += 8;
}

void terminal_print(const char *str, uint32_t color) {
    while (*str) { terminal_putc(*str, color); str++; }
}

