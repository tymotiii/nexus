void init_syscall(void) {
    uint32_t EFER_MSR = 0xC0000080;
    uint32_t STAR_MSR = 0xC0000081;
    uint32_t LSTAR_MSR = 0xC0000082;

    uint64_t efer;
    __asm__ volatile("rdmsr" : "=a"(efer) : "c"(EFER_MSR) : "rdx");
    efer |= 1;
    __asm__ volatile("wrmsr" : : "a"((uint32_t)efer), "d"((uint32_t)(efer >> 32)), "c"(EFER_MSR));

    uint32_t low = 0;
    uint32_t high = (0x08) | (0x20 << 16);
    __asm__ volatile("wrmsr" : : "a"(low), "d"(high), "c"(STAR_MSR));

    uint64_t stub_addr = (uint64_t)syscall_stub;
    __asm__ volatile("wrmsr" : : "a"((uint32_t)stub_addr), "d"((uint32_t)(stub_addr >> 32)), "c"(LSTAR_MSR));
}

