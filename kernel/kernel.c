#define MAX_TASKS 4
#define STACK_SIZE 16384

typedef struct {
    uint64_t rsp;
    uint8_t  stack[STACK_SIZE] __attribute__((aligned(16)));
    int      active;
} task_t;

static task_t task_list[MAX_TASKS];
int current_task_id = 0;              // NON-STATIC for asm linkage
static int total_tasks = 0;
static volatile int scheduler_ready = 0;

void kbd_push_char(char c) {
    __asm__ volatile("cli");
    int next = (kbd_head + 1) % KBD_BUFFER_SIZE;
    if (next != kbd_tail) { kbd_buffer[kbd_head] = c; kbd_head = next; }
    __asm__ volatile("sti");
}

char kbd_pop_char(void) {
    __asm__ volatile("cli");
    if (kbd_head == kbd_tail) {
        __asm__ volatile("sti");
        return 0;
    }
    char c = kbd_buffer[kbd_tail]; kbd_tail = (kbd_tail + 1) % KBD_BUFFER_SIZE;
    __asm__ volatile("sti");
    return c;
}

