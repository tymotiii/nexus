void create_kernel_task(void (*entry_point)(void)) {
    if (total_tasks >= MAX_TASKS) return;
    task_t *t = &task_list[total_tasks];
    t->active = 1;

    uint64_t *stack_top = (uint64_t *)&t->stack[STACK_SIZE];

    stack_top--; *stack_top = 0x10;                          // SS
    stack_top--; *stack_top = (uint64_t)&t->stack[STACK_SIZE - 16]; // RSP
    stack_top--; *stack_top = 0x202;                         // RFLAGS (IF=1)
    stack_top--; *stack_top = 0x08;                          // CS
    stack_top--; *stack_top = (uint64_t)entry_point;         // RIP

    for (int i = 0; i < 15; i++) { stack_top--; *stack_top = 0; }
    t->rsp = (uint64_t)stack_top;
    total_tasks++;
}

void create_user_task(void (*entry_point)(void)) {
    if (total_tasks >= MAX_TASKS) return;
    task_t *t = &task_list[total_tasks];
    t->active = 1;

    uint64_t *stack_top = (uint64_t *)&t->stack[STACK_SIZE];
    stack_top--; *stack_top = 0x1B;                          // SS (RPL=3)
    stack_top--; *stack_top = (uint64_t)&t->stack[STACK_SIZE - 16]; // RSP
    stack_top--; *stack_top = 0x202;                         // RFLAGS (IF=1)
    stack_top--; *stack_top = 0x23;                          // CS (RPL=3)
    stack_top--; *stack_top = (uint64_t)entry_point;         // RIP

    for (int i = 0; i < 15; i++) { stack_top--; *stack_top = 0; }
    t->rsp = (uint64_t)stack_top;
    total_tasks++;
}

