void _start(void) {
    if (limine_base_revision[2] == 1) halt();
    if (framebuffer_request.response == NULL || framebuffer_request.response->framebuffer_count < 1) halt();

    struct limine_framebuffer *fb = framebuffer_request.response->framebuffers[0];
    fb_address = (uint32_t*)fb->address;
    pitch_in_bytes = fb->pitch;
    fb_width = fb->width;
    fb_height = fb->height;

    clear_screen(0x00000000);
    terminal_print("[nexus] boot: initialization layer sequence started\n", 0xFFFFFF);

    init_gdt();
    init_idt();
    init_pic();
    init_pit();
    init_syscall();
    init_scheduler();

    if (module_request.response != NULL && module_request.response->module_count > 0) {
        struct limine_file *initramfs_file = module_request.response->modules[0];
        load_and_exec_from_tar((uint64_t)initramfs_file->address, initramfs_file->size, "./etc/test.bin");
    }

    terminal_print("[nexus] boot: global system interrupt unmask\n", 0xFFFFFF);
    __asm__ volatile("sti");

    task_idle();
}
