static uint32_t *fb_address;
static uint64_t pitch_in_bytes;
static uint64_t fb_width;
static uint64_t fb_height;

static int start_x = 0;
static int start_y = 0;
static volatile int cursor_x;
static volatile int cursor_y;

static void halt(void) {
    while (1) { __asm__("hlt"); }
}

static inline void outb(uint16_t port, uint8_t val) {
    __asm__ volatile ("outb %0, %1" : : "a"(val), "Nd"(port));
}

static inline uint8_t inb(uint16_t port) {
    uint8_t ret;
    __asm__ volatile ("inb %1, %0" : "=a"(ret) : "Nd"(port));
    return ret;
}

void clear_screen(uint32_t color) {
    cursor_x = start_x;
    cursor_y = start_y;
    for (uint64_t y = 0; y < fb_height; y++) {
        for (uint64_t x = 0; x < fb_width; x++) {
            uint32_t *pixel = (uint32_t*)((uint8_t*)fb_address + (y * pitch_in_bytes) + (x * 4));
            *pixel = color;
        }
    }
}

void scroll_screen(void) {
    uint64_t line_bytes = fb_width * 4;
    uint8_t *src = (uint8_t*)fb_address + (16 * pitch_in_bytes);
    uint8_t *dst = (uint8_t*)fb_address;
    uint64_t copy_height = fb_height - 16;

    for (uint64_t y = 0; y < copy_height; y++) {
        uint64_t *d64 = (uint64_t*)(dst + (y * pitch_in_bytes));
        uint64_t *s64 = (uint64_t*)(src + (y * pitch_in_bytes));
        for (uint64_t i = 0; i < line_bytes / 8; i++) {
            d64[i] = s64[i];
        }
    }

    for (uint64_t y = copy_height; y < fb_height; y++) {
        uint32_t *pixel_line = (uint32_t*)((uint8_t*)fb_address + (y * pitch_in_bytes));
        for (uint64_t x = 0; x < fb_width; x++) {
            pixel_line[x] = 0x00000000;
        }
    }

    cursor_y = fb_height - 16;
    cursor_x = start_x;
}

__attribute__((sysv_abi)) void draw_char(uint64_t x, uint64_t y, char c, uint32_t color) {
    unsigned char idx = (unsigned char)c;
    if (idx > 127) idx = '?';
    const uint8_t *glyph = ibm_vga_font[idx];
    for (uint64_t cy = 0; cy < 16; cy++) {
        for (uint64_t cx = 0; cx < 8; cx++) {
            uint64_t screen_x = x + cx;
            uint64_t screen_y = y + cy;
            if (screen_x < fb_width && screen_y < fb_height) {
                volatile uint32_t *pixel = (volatile uint32_t*)((uint8_t*)fb_address + (screen_y * pitch_in_bytes) + (screen_x * 4));
                if (glyph[cy] & (1 << (7 - cx))) {
                    *pixel = color;
                } else if (c == ' ') {
                    *pixel = 0x00000000; // Explicitly wipe background for spaces
                }
            }
        }
    }
}

__attribute__((sysv_abi)) void terminal_putc(char c, uint32_t color) {
    if (c == '\n') {
        cursor_x = start_x;
        cursor_y += 16;
        if (cursor_y + 16 > (int)fb_height) {
            scroll_screen();
        }
        return;
    }

    if (cursor_x + 8 > (int)fb_width) {
        cursor_x = start_x;
        cursor_y += 16;
        if (cursor_y + 16 > (int)fb_height) {
            scroll_screen();
        }
    }

    draw_char((uint64_t)cursor_x, (uint64_t)cursor_y, c, color);
    cursor_x += 8;
}

void terminal_print(const char *str, uint32_t color) {
    while (*str) {
        terminal_putc(*str, color);
        str++;
    }
}

void terminal_print_hex(uint64_t value, uint32_t color) {
    char *hex_digits = "0123456789ABCDEF";
    char buffer[16];
    int i = 0;
    terminal_print("0x", color);
    if (value == 0) {
        terminal_putc('0', color);
        return;
    }
    while (value > 0) {
        buffer[i++] = hex_digits[value & 0xF];
        value >>= 4;
    }
    for (int j = i - 1; j >= 0; j--) {
        terminal_putc(buffer[j], color);
    }
}

int str_ncmp(const char *s1, const char *s2, size_t n) {
    while (n--) {
        if (*s1 != *s2) return (unsigned char)*s1 - (unsigned char)*s2;
        if (*s1 == 0) break;
        s1++; s2++;
    }
    return 0;
}

// =========================================================================
// KEYBOARD BUFFER SYSTEM WITH BACKSPACE SCOPE
// =========================================================================
static const char kbd_scancode_map[128] = {
    0,  27, '1', '2', '3', '4', '5', '6', '7', '8', '9', '0', '-', '=', '\b', /* 0x0E is Backspace mapped to '\b' */
  '\t', 'q', 'w', 'e', 'r', 't', 'y', 'u', 'i', 'o', 'p', '[', ']', '\n',
    0,  'a', 's', 'd', 'f', 'g', 'h', 'j', 'k', 'l', ';', '\'', '`',   0,
  '\\', 'z', 'x', 'c', 'v', 'b', 'n', 'm', ',', '.', '/',   0, '*',   0, ' '
};

#define KBD_BUFFER_SIZE 256
static volatile char kbd_buffer[KBD_BUFFER_SIZE];
static volatile int kbd_head = 0;
static volatile int kbd_tail = 0;

void kbd_push_char(char c) {
    int next = (kbd_head + 1) % KBD_BUFFER_SIZE;
    if (next != kbd_tail) {
        kbd_buffer[kbd_head] = c;
        kbd_head = next;
    }
}

char kbd_pop_char(void) {
    if (kbd_head == kbd_tail) return 0;
    char c = kbd_buffer[kbd_tail];
    kbd_tail = (kbd_tail + 1) % KBD_BUFFER_SIZE;
    return c;
}

// =========================================================================
// IDT STRUCTURES
// =========================================================================
struct idt_entry {
    uint16_t isr_low;
    uint16_t kernel_cs;
    uint8_t  ist;
    uint8_t  attributes;
    uint16_t isr_mid;
    uint32_t isr_high;
    uint32_t reserved;
} __attribute__((packed));

struct idtr {
    uint16_t limit;
    uint64_t base;
} __attribute__((packed));

static struct idt_entry idt[256];
static struct idtr idtr_pointer;

void idt_set_descriptor(uint8_t vector, void* isr, uint8_t flags) {
    uint64_t addr = (uint64_t)isr;
    idt[vector].isr_low    = addr & 0xFFFF;
    idt[vector].kernel_cs  = 0x28;
    idt[vector].ist        = 0;
    idt[vector].attributes = flags;
    idt[vector].isr_mid    = (addr >> 16) & 0xFFFF;
    idt[vector].isr_high   = (addr >> 32) & 0xFFFFFFFF;
    idt[vector].reserved   = 0;
}

// =========================================================================
// SCHEDULER (MUTED)
// =========================================================================
#define MAX_TASKS 3
#define STACK_SIZE 8192

typedef struct {
    uint64_t rsp;
    uint8_t  stack[STACK_SIZE];
    int      active;
} task_t;

static task_t task_list[MAX_TASKS];
static int current_task_id = 0;
static int total_tasks = 0;
static int scheduler_ready = 0;

uint64_t schedule_from_isr(uint64_t current_rsp) {
    if (!scheduler_ready || total_tasks < 2) return current_rsp;
    task_list[current_task_id].rsp = current_rsp;
    current_task_id = (current_task_id + 1) % total_tasks;
    return task_list[current_task_id].rsp;
}

void create_task(void (*entry_point)(void)) {
    if (total_tasks >= MAX_TASKS) return;
    task_t *t = &task_list[total_tasks];
    t->active = 1;
    uint64_t *stack_top = (uint64_t *)&t->stack[STACK_SIZE];
    stack_top--; *stack_top = 0x10;
    stack_top--; *stack_top = (uint64_t)&t->stack[STACK_SIZE - 16];
    stack_top--; *stack_top = 0x202;
    stack_top--; *stack_top = 0x28;
    stack_top--; *stack_top = (uint64_t)entry_point;
    for (int i = 0; i < 15; i++) { stack_top--; *stack_top = 0; }
    t->rsp = (uint64_t)stack_top;
    total_tasks++;
}

