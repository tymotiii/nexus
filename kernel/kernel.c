#define CMD_BUFFER_SIZE 64
static char cmd_line[CMD_BUFFER_SIZE];
static int cmd_idx = 0;

void shell_task(void) {
    terminal_print("\nWelcome to NexusOS (v0.5-pure-white-ring3)\n", 0xFFFFFF);
    terminal_print("nexus_sh> ", 0xFFFFFF);

    while(1) {
        char c = kbd_pop_char();
        if (c != 0) {
            if (c == '\b') {
                if (cmd_idx > 0) {
                    cmd_idx--;
                    if (cursor_x >= start_x + 8) {
                        cursor_x -= 8;
                        draw_char(cursor_x, cursor_y, ' ', 0xFFFFFF, 0x00000000);
                    }
                }
            } else if (c == '\n') {
                terminal_print("\n", 0xFFFFFF);
                cmd_line[cmd_idx] = '\0';

                if (str_ncmp(cmd_line, "echo ", 5) == 0) {
                    terminal_print(&cmd_line[5], 0xFFFFFF);
                    terminal_print("\n", 0xFFFFFF);
                } else if (cmd_idx > 0) {
                    terminal_print("nexus_sh: unknown token: ", 0xFFFFFF);
                    terminal_print(cmd_line, 0xFFFFFF);
                    terminal_print("\n", 0xFFFFFF);
                }

                cmd_idx = 0;
                terminal_print("nexus_sh> ", 0xFFFFFF);
            } else {
                if (cmd_idx < CMD_BUFFER_SIZE - 1) {
                    cmd_line[cmd_idx++] = c;
                    terminal_putc(c, 0xFFFFFF);
                }
            }
        }
        __asm__ volatile("hlt");
    }
}

void task_idle(void) {
    while(1) {
        __asm__ volatile("hlt");
    }
}

void init_scheduler(void) {
    create_kernel_task(task_idle);
    create_kernel_task(shell_task);
    scheduler_ready = 1;
}

